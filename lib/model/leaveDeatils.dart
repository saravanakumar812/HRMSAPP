class User {
  final int id;
  final String name;
  final DateTime dor;
  final DateTime dof;
  final DateTime dot;
  final String leaveType;
  final String status;
  final String days;

  User({
    required this.id,
    required this.name,
    required this.dor,
    required this.dof,
    required this.dot,
    required this.leaveType,
    required this.days,
    required this.status,
  });
}
